"use strict";

/**
 * Generates a Strands-framework AgentCore harness from a parsed manifest object.
 * Returns a map of filename → file content strings.
 */
function generateStrands(manifest, projectId) {
  const agentId = manifest.id || "my-agent";
  const agentName = manifest.name || "My Agent";
  const modelId = manifest.model?.modelId || "anthropic.claude-3-5-sonnet-20241022-v2:0";
  const systemPrompt = manifest.systemPrompt || "You are a helpful AI assistant.";
  const tools = manifest.tools || [];
  const knowledge = manifest.knowledge || [];
  const memory = manifest.memory || {};
  const version = manifest.version || "0.1.0";

  const toolIds = tools.map((t) => t.toolId || t).filter(Boolean);
  const kbIds = knowledge.map((k) => k.knowledgeBaseId || k).filter(Boolean);

  const files = {};

  // agent.py — pure agent logic, no HTTP concerns
  files["agent.py"] = `# agent.py
# Generated by Pegasus AI Platform
# Agent: ${agentName} v${version} | Project: ${projectId}
# DO NOT EDIT tool bindings directly — update manifest.yaml and regenerate

from strands import Agent
from strands.models import BedrockModel
from tools import get_project_tools
from memory import configure_memory

SYSTEM_PROMPT = """
${systemPrompt.trim()}
"""

def build_agent() -> Agent:
    return Agent(
        model=BedrockModel(
            model_id="${modelId}",
            region_name="us-east-1",
        ),
        tools=get_project_tools(),
        system_prompt=SYSTEM_PROMPT,
        memory=configure_memory(),
    )
`;

  // harness.py — AgentCore Runtime HTTP harness (the real entry point)
  // AgentCore Runtime runs this as a long-lived process; it POSTs events to port 8080.
  // IMPORTANT: No heavy imports at module level — the HTTP server must bind within 30 s
  // of process start (AgentCore's initialization timeout). The Strands agent is imported
  // and built lazily on the first real request.
  files["harness.py"] = `#!/usr/bin/env python3
# harness.py — AgentCore Runtime HTTP harness wrapper
# Generated by Pegasus AI Platform for agent: ${agentName}
#
# AgentCore Runtime protocol:
#   POST /        { "input": "...", "sessionId": "..." }  → { "output": "...", "sessionId": "..." }
#   GET  /health  → { "status": "ok" }

from http.server import HTTPServer, BaseHTTPRequestHandler
import json
import logging
import os
import sys

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s %(levelname)s %(message)s",
    stream=sys.stdout,
)
logger = logging.getLogger(__name__)

# Lazily initialised — imported on first POST so the server can start within 30 s
_agent = None

def get_agent():
    global _agent
    if _agent is None:
        logger.info("Lazy-loading agent modules…")
        from agent import build_agent  # deferred: triggers strands + boto3 imports
        logger.info("Initialising Strands agent…")
        _agent = build_agent()
        logger.info("Agent ready.")
    return _agent


class AgentCoreHandler(BaseHTTPRequestHandler):
    def do_GET(self):
        # Health-check endpoint — responds immediately, no agent needed
        self._send(200, json.dumps({"status": "ok", "agent": "${agentId}"}))

    def do_POST(self):
        try:
            content_length = int(self.headers.get("Content-Length", 0))
            raw_body = self.rfile.read(content_length)
            event = json.loads(raw_body) if raw_body else {}

            # AgentCore may send input as a string or as { "text": "..." }
            raw_input = event.get("input", "")
            if isinstance(raw_input, dict):
                input_text = raw_input.get("text", "") or raw_input.get("message", "")
            else:
                input_text = str(raw_input) if raw_input else event.get("message", "")

            session_id = event.get("sessionId", "")
            logger.info("Invoking agent | session=%s | input=%.120s", session_id, input_text)

            result = get_agent()(input_text)

            response_body = json.dumps({
                "output": str(result),
                "sessionId": session_id,
            })
            self._send(200, response_body)
            logger.info("Response sent | session=%s | output=%.120s", session_id, str(result))

        except Exception as exc:
            logger.exception("Agent invocation failed")
            self._send(500, json.dumps({"error": str(exc)}))

    def _send(self, code: int, body: str):
        encoded = body.encode("utf-8")
        self.send_response(code)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(encoded)))
        self.end_headers()
        self.wfile.write(encoded)

    def log_message(self, fmt, *args):
        logger.debug(fmt, *args)


if __name__ == "__main__":
    port = int(os.environ.get("PORT", 8080))
    server = HTTPServer(("0.0.0.0", port), AgentCoreHandler)
    logger.info("AgentCore harness listening on 0.0.0.0:%d — startup complete", port)
    server.serve_forever()
`;

  // tools.py
  const toolImports = toolIds.length > 0
    ? toolIds.map((id) => `    client.get_tool("${id}"),`).join("\n")
    : "    # No tools selected";

  files["tools.py"] = `# tools.py
# Generated by Pegasus AI Platform — project: ${projectId}

import boto3
from strands.tools.mcp import MCPClient

MCP_GATEWAY_BASE = "arn:aws:bedrock:us-east-1:ACCOUNT_ID:agentcore/gateway"

def _get_auth(secret_ref: str) -> str:
    client = boto3.client("secretsmanager")
    return client.get_secret_value(SecretId=secret_ref)["SecretString"]

def get_project_tools() -> list:
    """Return all project-scoped tools bound at authoring time."""
${toolIds.length === 0 ? "    return []" : `    client = MCPClient(
        endpoint=f"{MCP_GATEWAY_BASE}/${projectId}-tools",
    )
    return [
${toolImports}
    ]`}
`;

  // memory.py
  files["memory.py"] = `# memory.py
# Generated by Pegasus AI Platform

from strands.memory import AgentCoreMemory

def configure_memory():
    ${memory.shortTerm || memory.longTerm ? `return AgentCoreMemory(
        memory_id="${agentId}-memory",
        short_term=${memory.shortTerm ? "True" : "False"},
        long_term=${memory.longTerm ? "True" : "False"},
        session_scope="${memory.sessionScope || "USER_ID"}",
    )` : "return None"}
`;

  // harness.yaml
  files["harness.yaml"] = `# harness.yaml — AgentCore Runtime configuration
# Generated by Pegasus AI Platform

agentcore:
  runtime:
    handler: agent.handler
    memory:
      memoryId: ${agentId}-memory
      sessionScope: USER_ID
    identity:
      authMode: IAM
  observability:
    cloudwatch:
      logGroup: /pegasus/agents/${agentId}
    traceLevel: ${manifest.observability?.traceLevel || "standard"}
`;

  // requirements.txt
  files["requirements.txt"] = `strands-agents>=0.1.0
boto3>=1.34.0
`;

  // Dockerfile
  files["Dockerfile"] = `FROM public.ecr.aws/lambda/python:3.12

COPY requirements.txt .
RUN pip install -r requirements.txt --target /var/task

COPY *.py /var/task/

CMD ["agent.handler"]
`;

  // tests/test_agent.py
  files["tests/test_agent.py"] = `# test_agent.py — Unit tests for ${agentName}
# Generated by Pegasus AI Platform

from unittest.mock import MagicMock, patch
import pytest

@patch("agent.get_project_tools", return_value=[])
@patch("agent.configure_memory", return_value=None)
def test_handler_returns_output(mock_memory, mock_tools):
    # Patch the agent call
    with patch("agent.Agent") as MockAgent:
        instance = MockAgent.return_value
        instance.return_value = "Test response"

        from agent import handler
        result = handler({"input": "Hello", "sessionId": "test-session"}, None)

        assert "output" in result
        assert result["sessionId"] == "test-session"

def test_handler_missing_input():
    with patch("agent.Agent") as MockAgent:
        instance = MockAgent.return_value
        instance.return_value = ""

        from agent import handler
        result = handler({}, None)
        assert "output" in result
`;

  // README.md
  files["README.md"] = `# ${agentName}

> Generated by Pegasus AI Platform on ${new Date().toISOString().split("T")[0]}

**Project:** ${projectId}
**Version:** ${version}
**Framework:** Strands (AWS AgentCore)
**Model:** ${modelId}

## Tools (${toolIds.length})

${toolIds.length > 0 ? toolIds.map((t) => `- \`${t}\``).join("\n") : "_No tools selected_"}

## Knowledge Bases (${kbIds.length})

${kbIds.length > 0 ? kbIds.map((k) => `- \`${k}\``).join("\n") : "_No knowledge bases selected_"}

## Local Development

\`\`\`bash
pip install -r requirements.txt
python -c "from agent import build_agent; a = build_agent(); print(a('Hello'))"
\`\`\`

## Deploy

Deployment is managed by the Pegasus CI/CD pipeline via the Approval Workflow.
Do not deploy manually — submit via the platform and let the approval flow handle it.
`;

  return files;
}

module.exports = { generateStrands };
