"use strict";

/**
 * Generates an AgentCore-compatible wrapper for externally built CrewAI packages.
 * The wrapper adapts any entry point to the AgentCore handler(event, context) convention.
 */
function generateAgentCoreWrapper(manifest) {
  const entryPointFile = manifest.entryPoint || "app.py";
  const entryFunction  = manifest.entryFunction || "handler";
  const moduleName     = entryPointFile.replace(/\.py$/, "").replace(/\//g, ".");

  const needsWrapper = entryFunction !== "handler";

  const wrapperPy = needsWrapper ? `# AUTO-GENERATED by Guardian AI Platform — do not edit manually
# Wraps '${entryFunction}' in '${moduleName}' for Bedrock AgentCore Runtime compatibility.
import json
import sys
import traceback

sys.path.insert(0, ".")

try:
    from ${moduleName} import ${entryFunction} as _crew_entry
except ImportError as e:
    raise RuntimeError(f"Failed to import entry point '${entryFunction}' from '${moduleName}': {e}")


def handler(event, context):
    """Bedrock AgentCore Runtime entry point — auto-generated wrapper."""
    try:
        result = _crew_entry(event, context)
        if isinstance(result, dict):
            return result
        return {"statusCode": 200, "body": json.dumps({"result": str(result)})}
    except Exception:
        error_detail = traceback.format_exc()
        return {"statusCode": 500, "body": json.dumps({"error": error_detail})}
` : null;

  const dockerfileContent = `# AUTO-GENERATED by Guardian AI Platform
FROM public.ecr.aws/lambda/python:${manifest.pythonVersion || "3.12"}

WORKDIR /var/task

# Install dependencies
COPY ${manifest.dependencyFile || "requirements.txt"} .
RUN pip install --no-cache-dir -r ${manifest.dependencyFile || "requirements.txt"}

# Copy package
COPY . .
${needsWrapper ? "\n# AgentCore wrapper generated by Guardian AI Platform\nCOPY agentcore_wrapper.py .\n" : ""}
# AgentCore Runtime entry point
CMD ["${needsWrapper ? "agentcore_wrapper" : moduleName}.handler"]
`;

  const agentCoreSpec = {
    wrapperGenerated: needsWrapper,
    wrapperFile: needsWrapper ? "agentcore_wrapper.py" : null,
    effectiveEntryPoint: needsWrapper ? "agentcore_wrapper.handler" : `${moduleName}.handler`,
    runtimeConfig: {
      memoryMb: 2048,
      timeoutSeconds: 300,
      executionRoleArn: manifest.executionRoleArn || null,
    },
    modelConfig: {
      modelId: manifest.modelId || null,
      crossAccountRoleArn: manifest.crossAccountRoleArn || null,
    },
    toolGatewayConfig: (manifest.toolIds || []).map((toolId) => ({
      toolId,
      gatewayEndpoint: null, // resolved from project tool catalog at deploy time
    })),
    inputSchema:  manifest.inputSchema  || null,
    outputSchema: manifest.outputSchema || null,
    generatedAt: new Date().toISOString(),
  };

  const files = {};
  if (needsWrapper) files["agentcore_wrapper.py"] = wrapperPy;
  files["Dockerfile.agentcore"] = dockerfileContent;

  return { files, agentCoreSpec };
}

module.exports = { generateAgentCoreWrapper };
